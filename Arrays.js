//Creating an Array
var QA_Intern =["Neha", "Awachar", "B.E.", "SDET+"];
console.log(QA_Intern);
console.log(QA_Intern[0]);

//Using the JavaScript Keyword new
var QA_Intern =new Array("Neha", "Awachar", "B.E.", "SDET+");
console.log(QA_Intern);

//Access the Elements of an Array
var QA_Intern =new Array("Neha", "Awachar", "B.E.", "SDET+");
console.log(QA_Intern);
console.log("First element of Array "+QA_Intern[0]);
console.log("Second element of Array "+QA_Intern[1]);
console.log("Third element of Array "+QA_Intern[2]);
console.log("Fourth element of Array "+QA_Intern[3]);

//Changing an Array Element
var QA_Intern =new Array("Neha", "Awachar", "B.E.", "SDET+");
console.log(QA_Intern);
console.log("First element of Array "+QA_Intern[0]);
QA_Intern[0]="Suhani";
console.log("First element of Array "+QA_Intern[0]);

//The length Property & accessing last array element
var QA_Intern =new Array("Neha", "Awachar", "B.E.", "SDET+");
console.log(QA_Intern.length);
console.log(QA_Intern[QA_Intern.length-1]);

